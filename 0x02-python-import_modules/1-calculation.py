#!/usr/bin/python3
def add(a, b):
    """Addition function

    Args:
        a: integer 1
        b: integer 2

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """Subtraction function

    Args:
        a: integer 1
        b: integer 2

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """Multiplication function

    Args:
        a: integer 1
        b: integer 2

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """My division function

    Args:
        a: integer 1
        b: integer 2

    Returns:
        The return value. a / b
    """
    return int(a / b)
